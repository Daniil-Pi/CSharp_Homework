void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            //Console.WriteLine($"{image[i,j]} ");
            Thread.Sleep(3);
            if (image[i,j] == 0)
            System.Console.Write($" ");
            else if (image[i,j] == 1)
            System.Console.Write($"+");
            else if (image[i,j] == 2)
            {
            Console.ForegroundColor = ConsoleColor.Red;
            System.Console.Write($"+");
            Console.ResetColor();
            }
        }
        Console.WriteLine();
    }
}

void FillImage(int str, int kol)
{
    if (pic[str,kol] == 0)
    {
        pic[str,kol] = 2;
        
        
        FillImage(str,kol-1);
        FillImage(str-1,kol);
        FillImage(str,kol+1);
        FillImage(str+1,kol);
        
    }

}

